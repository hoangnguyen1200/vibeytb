import 'dotenv/config';
import { QueueName } from '../types/queue';
import { addJobToQueue } from '../lib/queue/producer';
import { supabase } from '../lib/supabase/client';

async function fireTest() {
  console.log('🤖 Bắn Job Test Trigger Gemini...');

  // 1. Tạo 1 Trend giả lập trực tiếp vào DB
  const { data: trend, error: tErr } = await supabase
    .from('trends')
    .insert({
       keyword: 'AI Video Generator Tools 2026',
       search_volume: 500000,
       source: 'manual_test'
    })
    .select('id')
    .single();

  if (tErr) {
    console.error('Lỗi tạo trend:', tErr.message);
    process.exit(1);
  }

  // 2. Tạo Project từ Trend đó
  const { data: project, error: pErr } = await supabase
    .from('video_projects')
    .insert({
       trend_id: trend?.id,
       status: 'draft'
    })
    .select('id')
    .single();

  if (pErr || !project) {
    console.error('Lỗi tạo mồi project:', pErr?.message);
    process.exit(1);
  }

  console.log(`📌 Found Project ID để test: ${project.id}`);

  // 3. Đẩy Job vào hàng đợi AI
  await addJobToQueue(QueueName.AI_GENERATION, 'generate_script_test', { project_id: project.id });
  
  console.log('✅ Đã nạp đạn Job thành công vào Hàng Đợi AI_GENERATION!');
  
  // Đợi Gemini xử lý khoảng 15 giây
  console.log('⏳ Đợi Worker 15 giây để gọi Gemini và ghi Database...');
  await new Promise(resolve => setTimeout(resolve, 15000));

  // 4. Lấy kịch bản ra xem thành quả
  const { data: script, error: sErr } = await supabase
    .from('video_scripts')
    .select('*')
    .eq('project_id', project.id)
    .single();

  if (sErr || !script) {
     console.error('❌ Thất bại! Worker chưa kịp trả Kịch bản JSON về hoặc bị lỗi API.');
     console.error('Lỗi DB nếu có:', sErr?.message);
  } else {
     console.log('\n======================================================');
     console.log(`✅ LẤY THÀNH CÔNG KỊCH BẢN TỪ DATABASE`);
     console.log('------------------------------------------------------');
     console.log('🎞️ SCENES GENERATED BY GEMINI 1.5 PRO:');
     console.dir(script.scenes, { depth: null, colors: true });
     console.log('======================================================\n');
  }
}

fireTest();
